CREATE OR REPLACE FUNCTION "public"."search_problems_by_embedding"("query_embedding" "public"."vector", "match_count" integer DEFAULT 20) RETURNS TABLE("id" "uuid", "problem_filename" "text", "answer_filename" "text", "answer" integer, "chapter_id" "uuid", "difficulty" character varying, "problem_type" character varying, "correct_rate" integer, "exam_year" integer, "created_at" timestamp with time zone, "updated_at" timestamp with time zone, "similarity" double precision)
    LANGUAGE "sql" STABLE
    AS $$
    SELECT
      p.id,
      p.problem_filename,
      p.answer_filename,
      p.answer,
      p.chapter_id,
      p.difficulty,
      p.problem_type,
      p.correct_rate,
      p.exam_year,
      p.created_at,
      p.updated_at,
      1 - (p.problem_embedding <=> query_embedding) as similarity
    FROM problems p
    WHERE p.problem_embedding IS NOT NULL
    ORDER BY p.problem_embedding <=> query_embedding
    LIMIT match_count;
  $$;


